package network;

public class Person {

    private int age;
    private String name;
    private boolean isAmerican;

    public Person(){}

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public boolean isAmerican() {
        return isAmerican;
    }

    public void setAmerican(boolean american) {
        isAmerican = american;
    }
}
